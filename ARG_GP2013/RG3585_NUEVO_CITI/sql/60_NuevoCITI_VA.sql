				WHERE TX.ColumnaArchivo = 2 AND A.TAXAMNT <> 0
				) A 
			GROUP BY A.DOCNUMBR, A.RMDTYPAL, A.CUSTNMBR 
			) TX ON A.DOCNUMBR = TX.DOCNUMBR AND A.RMDTYPAL = TX.RMDTYPAL AND A.CUSTNMBR = TX.CUSTNMBR

